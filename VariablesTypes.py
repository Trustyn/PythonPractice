# Define an intiger
myint = 7
print(myint)

# Define a float
myfloat = 7.0
print(myfloat)
myfloat = float(7)
print(myfloat)

# Define a string
# both methods to define a string work except when using double quotes
# you can use apostrophes and they won't terminate your definition.
mystring = 'hello'
print(mystring)
mystring = "hello"
print(mystring)

# Simple Oporators
one = 1
two = 2
three = one + two
print(three)
hello = "hello"
world = "world"
helloworld = hello + " " + world
print(helloworld)

# Assignments simultaneously
a, b = 3, 4
print(a, b)

